#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*new_string;
	size_t	s_len;

	if (!s)
		return (NULL);
	s_len = ft_strlen(s);
	if (start >= s_len)
		return (ft_strdup(""));
	if (len > s_len - start)
		len = s_len - start;
	new_string = malloc(len + 1 * sizeof(char));
	if (!new_string)
		return (NULL);
	ft_strlcpy(new_string, s + start, len + 1);
	return (new_string);
}
